package com.volvo.java.StringMethods;

public class StringStartsWith_EndsWith {

    public static void main(String[] args) {
        
        String s="Muzammil";
        
        System.out.println(s.startsWith("M"));
        System.out.println(s.startsWith("m"));
        System.out.println();
        System.out.println(s.endsWith("il"));
        System.out.println(s.endsWith("L"));
    }
}
