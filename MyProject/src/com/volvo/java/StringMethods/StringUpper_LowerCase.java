package com.volvo.java.StringMethods;

public class StringUpper_LowerCase {

    public static void main(String[] args) {
        
        String s="MUZammil";
        
        System.out.println(s.toUpperCase());
        System.out.println(s);
        System.out.println(s.toLowerCase());
        System.out.println(s);
    }
}
